// TYPE CASTING (fundição de tipo)

// Casting NUMBER to STRING
    // here is the variable with a number  
let num = 1;
console.log(`${num} type: ${typeof(num)}`);

    // casting moment
num = String(num);
console.log(`${num} type: ${typeof(num)}`);


// Casting STRING to NUMBER
    // here you can see a variable with a string
let text = "1.3"; 
console.log(`${text} type: ${typeof(text)}`);

    // the casting moment
text = Number(text);
console.log(`${text} type: ${typeof(text)}`);
